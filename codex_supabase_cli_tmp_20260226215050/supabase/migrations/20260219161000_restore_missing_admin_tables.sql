-- -----------------------------------------------------
-- JOB RUNS
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS public.job_runs (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  action text NOT NULL,
  status text NOT NULL DEFAULT 'unknown',
  dedupe_key text,
  result jsonb,
  error_message text,
  executed_at timestamptz,
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_job_runs_action ON public.job_runs(action);
CREATE INDEX IF NOT EXISTS idx_job_runs_status ON public.job_runs(status);
CREATE INDEX IF NOT EXISTS idx_job_runs_created_at ON public.job_runs(created_at DESC);

ALTER TABLE public.job_runs ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "job_runs_select_all" ON public.job_runs;
CREATE POLICY "job_runs_select_all"
  ON public.job_runs
  FOR SELECT
  USING (true);

-- -----------------------------------------------------
-- REPORTS
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS public.reports (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  type text,
  report_type text,
  category text,
  status text NOT NULL DEFAULT 'Nouveau',
  state text,
  reporter_pseudo text,
  reporter_name text,
  reporter_email text,
  reporter_id uuid,
  reported_content text,
  content_title text,
  annonce_title text,
  listing_title text,
  reported_user text,
  reported_user_id uuid,
  target_user text,
  target_user_pseudo text,
  description text,
  reason text,
  details text,
  message text,
  evidence text[] NOT NULL DEFAULT ARRAY[]::text[],
  evidence_urls text[] NOT NULL DEFAULT ARRAY[]::text[],
  attachments text[] NOT NULL DEFAULT ARRAY[]::text[],
  submitted_at timestamptz,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_reports_created_at ON public.reports(created_at DESC);
CREATE INDEX IF NOT EXISTS idx_reports_status ON public.reports(status);
