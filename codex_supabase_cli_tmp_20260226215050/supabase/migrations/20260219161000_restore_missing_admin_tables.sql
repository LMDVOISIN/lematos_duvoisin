DROP POLICY IF EXISTS "reports_select_all" ON public.reports;
CREATE POLICY "reports_select_all"
  ON public.reports
  FOR SELECT
  USING (true);

-- -----------------------------------------------------
-- SUBCATEGORIES (with FK relation for PostgREST embedding)
-- -----------------------------------------------------
DO $$
DECLARE
  categories_id_type text;
BEGIN
  SELECT format_type(a.atttypid, a.atttypmod)
  INTO categories_id_type
  FROM pg_attribute a
  JOIN pg_class c ON c.oid = a.attrelid
  JOIN pg_namespace n ON n.oid = c.relnamespace
  WHERE n.nspname = 'public'
    AND c.relname = 'categories'
    AND a.attname = 'id'
    AND a.attnum > 0
    AND NOT a.attisdropped;

  IF categories_id_type IS NULL THEN
    RAISE EXCEPTION 'public.categories.id introuvable';
  END IF;

  EXECUTE format(
    'CREATE TABLE IF NOT EXISTS public.subcategories (
      id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      category_id %s NOT NULL,
      name text NOT NULL,
      nom text,
      slug text,
      icon_url text,
      created_at timestamptz NOT NULL DEFAULT now(),
      updated_at timestamptz NOT NULL DEFAULT now()
    )',
    categories_id_type
  );
END $$;

DO $$
BEGIN
  ALTER TABLE public.subcategories
    ADD CONSTRAINT subcategories_category_id_fkey
    FOREIGN KEY (category_id)
    REFERENCES public.categories(id)
    ON DELETE CASCADE;
EXCEPTION
  WHEN duplicate_object THEN
    NULL;
END $$;

CREATE INDEX IF NOT EXISTS idx_subcategories_category_id ON public.subcategories(category_id);
CREATE INDEX IF NOT EXISTS idx_subcategories_name ON public.subcategories(name);
CREATE UNIQUE INDEX IF NOT EXISTS uq_subcategories_category_name ON public.subcategories(category_id, name);

ALTER TABLE public.subcategories ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "subcategories_select_all" ON public.subcategories;
CREATE POLICY "subcategories_select_all"
  ON public.subcategories
  FOR SELECT
  USING (true);

-- Optional compatibility alias used by some environments
DO $$
DECLARE
  rel_kind "char";
BEGIN
  SELECT c.relkind
  INTO rel_kind
  FROM pg_class c
  JOIN pg_namespace n ON n.oid = c.relnamespace
  WHERE n.nspname = 'public'
    AND c.relname = 'signalements';

  IF rel_kind IS NULL THEN
    EXECUTE 'CREATE VIEW public.signalements AS SELECT * FROM public.reports';
  ELSIF rel_kind = 'v' THEN
    EXECUTE 'CREATE OR REPLACE VIEW public.signalements AS SELECT * FROM public.reports';
  ELSE
    RAISE NOTICE 'public.signalements existe deja et n''est pas une vue; alias ignore';
  END IF;
END $$;

