-- Official inspection model (completion):
-- - 24h dispute window after end-of-rental inspection closure
-- - strict dispute opening (selected photos + detailed description + deadline)
-- - internal arbitration scope (platform-only evidence rules)
-- - automatic settlement state transition: hold -> release / moderation freeze

CREATE TABLE IF NOT EXISTS public.reservation_inspection_settlements (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  reservation_id uuid NOT NULL REFERENCES public.reservations(id) ON DELETE CASCADE,
  end_session_id bigint REFERENCES public.reservation_inspection_sessions(id) ON DELETE SET NULL,
  status text NOT NULL DEFAULT 'pending_end_inspection',
  payment_hold_status text NOT NULL DEFAULT 'none',
  hold_started_at timestamptz,
  contest_window_ends_at timestamptz,
  auto_release_eligible_at timestamptz,
  dispute_opened_at timestamptz,
  payment_frozen_at timestamptz,
  payment_released_at timestamptz,
  final_decision_at timestamptz,
  dispute_count integer NOT NULL DEFAULT 0,
  latest_dispute_id bigint,
  legal_scope_note text NOT NULL DEFAULT 'Pour l?arbitrage interne de la plateforme, seules les photos d?etat des lieux prises via le module officiel et les traces techniques associees sont prises en compte.',
  metadata jsonb NOT NULL DEFAULT '{}'::jsonb,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT reservation_inspection_settlements_unique_reservation UNIQUE (reservation_id),
  CONSTRAINT reservation_inspection_settlements_status_check CHECK (
    status IN (
      'pending_end_inspection',
      'hold_24h',
      'disputed_pending_moderation',
      'released_no_dispute',
      'released_after_moderation',
      'captured_after_moderation'
    )
  ),
  CONSTRAINT reservation_inspection_settlements_payment_hold_status_check CHECK (
    payment_hold_status IN ('none', 'hold_24h', 'frozen_for_moderation', 'released', 'captured')
  )
);

CREATE INDEX IF NOT EXISTS idx_reservation_inspection_settlements_status_due
  ON public.reservation_inspection_settlements(status, auto_release_eligible_at);

CREATE INDEX IF NOT EXISTS idx_reservation_inspection_settlements_reservation
  ON public.reservation_inspection_settlements(reservation_id);

CREATE TABLE IF NOT EXISTS public.reservation_inspection_disputes (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  reservation_id uuid NOT NULL REFERENCES public.reservations(id) ON DELETE CASCADE,
  settlement_id bigint REFERENCES public.reservation_inspection_settlements(id) ON DELETE SET NULL,
  opened_by_user_id uuid NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
  opened_by_role text NOT NULL,
  status text NOT NULL DEFAULT 'opened',
  title text,
  description text NOT NULL,
  opened_at timestamptz NOT NULL DEFAULT now(),
  window_ends_at timestamptz,
  submitted_within_window boolean NOT NULL DEFAULT true,
  moderator_user_id uuid REFERENCES public.profiles(id) ON DELETE SET NULL,
  moderator_decision text,
  moderator_note text,
  resolved_at timestamptz,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT reservation_inspection_disputes_opened_by_role_check CHECK (opened_by_role IN ('owner', 'renter')),
  CONSTRAINT reservation_inspection_disputes_status_check CHECK (
    status IN (
      'opened',
      'under_review',
      'pending_information',
      'resolved_release',
      'resolved_capture',
      'rejected',
      'withdrawn'
    )
  ),
  CONSTRAINT reservation_inspection_disputes_moderator_decision_check CHECK (
    moderator_decision IS NULL OR moderator_decision IN ('release', 'capture', 'reject')
  )
);

CREATE INDEX IF NOT EXISTS idx_reservation_inspection_disputes_reservation
  ON public.reservation_inspection_disputes(reservation_id, opened_at DESC);

CREATE INDEX IF NOT EXISTS idx_reservation_inspection_disputes_status
  ON public.reservation_inspection_disputes(status, opened_at DESC);

CREATE TABLE IF NOT EXISTS public.reservation_inspection_dispute_photos (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  dispute_id bigint NOT NULL REFERENCES public.reservation_inspection_disputes(id) ON DELETE CASCADE,
  reservation_id uuid NOT NULL REFERENCES public.reservations(id) ON DELETE CASCADE,
  photo_id_text text NOT NULL,
  photo_phase text,
  photo_url_snapshot text,
  selected_at timestamptz NOT NULL DEFAULT now(),
  metadata jsonb NOT NULL DEFAULT '{}'::jsonb,
  CONSTRAINT reservation_inspection_dispute_photos_unique UNIQUE (dispute_id, photo_id_text)
);

CREATE INDEX IF NOT EXISTS idx_reservation_inspection_dispute_photos_dispute
  ON public.reservation_inspection_dispute_photos(dispute_id);

