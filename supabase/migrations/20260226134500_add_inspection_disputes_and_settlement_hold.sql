-- Allow cleanup deletion of frozen inspection photos only after final settlement.
CREATE OR REPLACE FUNCTION public.enforce_reservation_photo_phase_freeze()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public, auth, pg_temp
AS $$
DECLARE
  v_reservation_id uuid;
  v_phase text;
  v_closed_at timestamptz;
  v_settlement_status text;
BEGIN
  IF TG_OP = 'DELETE' THEN
    v_reservation_id := OLD.reservation_id;
    v_phase := lower(coalesce(OLD.phase, ''));
  ELSE
    v_reservation_id := NEW.reservation_id;
    v_phase := lower(coalesce(NEW.phase, ''));
  END IF;

  SELECT s.closed_at
  INTO v_closed_at
  FROM public.reservation_inspection_sessions s
  WHERE s.reservation_id = v_reservation_id
    AND s.phase = v_phase
    AND s.closed_at IS NOT NULL
  ORDER BY s.closed_at DESC
  LIMIT 1;

  IF v_closed_at IS NOT NULL THEN
    IF TG_OP = 'DELETE' THEN
      SELECT lower(coalesce(st.status, ''))
      INTO v_settlement_status
      FROM public.reservation_inspection_settlements st
      WHERE st.reservation_id = v_reservation_id
      ORDER BY st.id DESC
      LIMIT 1;

      IF v_settlement_status IN ('released_no_dispute', 'released_after_moderation', 'captured_after_moderation') THEN
        RETURN OLD;
      END IF;
    END IF;

    RAISE EXCEPTION 'Les photos de cette phase d?etat des lieux sont fig?es apr?s cl?ture';
  END IF;

  IF TG_OP = 'DELETE' THEN
    RETURN OLD;
  END IF;

  RETURN NEW;
END;
$$;
