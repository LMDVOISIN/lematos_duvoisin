-- Reinforced inspection workflow (etat des lieux):
-- - mutual presence confirmation with attestation + technical trace
-- - per-phase inspection session state machine (start/end)
-- - explicit photo finalization by each party
-- - automatic phase closure + 24h contest window
-- - server-side timestamping and freezing of reservation photos after closure

CREATE TABLE IF NOT EXISTS public.reservation_inspection_sessions (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  reservation_id uuid NOT NULL REFERENCES public.reservations(id) ON DELETE CASCADE,
  phase text NOT NULL,
  status text NOT NULL DEFAULT 'awaiting_presence',
  owner_presence_confirmed_at timestamptz,
  renter_presence_confirmed_at timestamptz,
  owner_photos_finalized_at timestamptz,
  renter_photos_finalized_at timestamptz,
  closed_at timestamptz,
  contest_window_ends_at timestamptz,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT reservation_inspection_sessions_phase_check CHECK (phase IN ('start', 'end')),
  CONSTRAINT reservation_inspection_sessions_status_check CHECK (status IN ('awaiting_presence', 'awaiting_finalization', 'closed')),
  CONSTRAINT reservation_inspection_sessions_unique_reservation_phase UNIQUE (reservation_id, phase)
);

CREATE INDEX IF NOT EXISTS idx_reservation_inspection_sessions_reservation_phase
  ON public.reservation_inspection_sessions(reservation_id, phase);

CREATE INDEX IF NOT EXISTS idx_reservation_inspection_sessions_closed_at
  ON public.reservation_inspection_sessions(closed_at DESC NULLS LAST);

CREATE TABLE IF NOT EXISTS public.reservation_inspection_presence_confirmations (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  reservation_id uuid NOT NULL REFERENCES public.reservations(id) ON DELETE CASCADE,
  phase text NOT NULL,
  user_id uuid NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
  participant_role text NOT NULL,
  attestation_text text NOT NULL,
  attestation_accepted boolean NOT NULL DEFAULT true,
  confirmed_at_server timestamptz NOT NULL DEFAULT now(),
  ip_address text,
  user_agent text,
  device_fingerprint_hash text,
  device_type text,
  client_reported_coordinates text,
  metadata jsonb NOT NULL DEFAULT '{}'::jsonb,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT reservation_inspection_presence_phase_check CHECK (phase IN ('start', 'end')),
  CONSTRAINT reservation_inspection_presence_role_check CHECK (participant_role IN ('owner', 'renter')),
  CONSTRAINT reservation_inspection_presence_unique_role UNIQUE (reservation_id, phase, participant_role),
  CONSTRAINT reservation_inspection_presence_unique_user UNIQUE (reservation_id, phase, user_id)
);

CREATE INDEX IF NOT EXISTS idx_reservation_inspection_presence_reservation_phase
  ON public.reservation_inspection_presence_confirmations(reservation_id, phase);

ALTER TABLE public.reservation_inspection_sessions ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.reservation_inspection_presence_confirmations ENABLE ROW LEVEL SECURITY;

