CREATE OR REPLACE FUNCTION public.apply_reservation_inspection_session_state()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public, auth, pg_temp
AS $$
DECLARE
  v_now timestamptz := now();
BEGIN
  NEW.updated_at := v_now;

  IF NEW.closed_at IS NULL
     AND NEW.owner_presence_confirmed_at IS NOT NULL
     AND NEW.renter_presence_confirmed_at IS NOT NULL
     AND NEW.owner_photos_finalized_at IS NOT NULL
     AND NEW.renter_photos_finalized_at IS NOT NULL THEN
    NEW.closed_at := v_now;
    NEW.contest_window_ends_at := coalesce(NEW.contest_window_ends_at, v_now + interval '24 hours');
  END IF;

  IF NEW.closed_at IS NOT NULL THEN
    NEW.status := 'closed';
    NEW.contest_window_ends_at := coalesce(NEW.contest_window_ends_at, NEW.closed_at + interval '24 hours');
  ELSIF NEW.owner_presence_confirmed_at IS NOT NULL
     AND NEW.renter_presence_confirmed_at IS NOT NULL THEN
    NEW.status := 'awaiting_finalization';
  ELSE
    NEW.status := 'awaiting_presence';
  END IF;

  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_apply_reservation_inspection_session_state ON public.reservation_inspection_sessions;
CREATE TRIGGER trg_apply_reservation_inspection_session_state
BEFORE INSERT OR UPDATE ON public.reservation_inspection_sessions
FOR EACH ROW
EXECUTE FUNCTION public.apply_reservation_inspection_session_state();

