CREATE TRIGGER trg_freeze_phase_reservation_photos_on_session_close
AFTER INSERT OR UPDATE ON public.reservation_inspection_sessions
FOR EACH ROW
EXECUTE FUNCTION public.freeze_phase_reservation_photos_on_session_close();

CREATE OR REPLACE FUNCTION public.confirm_reservation_inspection_presence(
  p_reservation_id uuid,
  p_phase text,
  p_attestation_text text DEFAULT 'Je certifie etre physiquement present avec l?autre partie au moment de la prise des photos. Toute fausse declaration engage ma responsabilite.',
  p_ip_address text DEFAULT NULL,
  p_user_agent text DEFAULT NULL,
  p_device_fingerprint_hash text DEFAULT NULL,
  p_device_type text DEFAULT NULL,
  p_client_reported_coordinates text DEFAULT NULL,
  p_metadata jsonb DEFAULT '{}'::jsonb
)
RETURNS public.reservation_inspection_sessions
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public, auth, pg_temp
AS $$
DECLARE
  v_user_id uuid := auth.uid();
  v_role text := coalesce(auth.role(), '');
  v_reservation record;
  v_participant_role text;
  v_session public.reservation_inspection_sessions;
  v_phase text := lower(coalesce(p_phase, ''));
BEGIN
  IF v_role = 'anon' OR v_user_id IS NULL THEN
    RAISE EXCEPTION 'Authentification requise';
  END IF;

  IF v_phase NOT IN ('start', 'end') THEN
    RAISE EXCEPTION 'phase invalide';
  END IF;

  SELECT r.id, r.owner_id, r.renter_id
  INTO v_reservation
  FROM public.reservations r
  WHERE r.id = p_reservation_id;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Reservation introuvable';
  END IF;

  IF v_user_id = v_reservation.owner_id THEN
    v_participant_role := 'owner';
  ELSIF v_user_id = v_reservation.renter_id THEN
    v_participant_role := 'renter';
  ELSE
    RAISE EXCEPTION 'Utilisateur non autorise pour cette reservation';
  END IF;

  INSERT INTO public.reservation_inspection_presence_confirmations (
    reservation_id,
    phase,
    user_id,
    participant_role,
    attestation_text,
    attestation_accepted,
    confirmed_at_server,
    ip_address,
    user_agent,
    device_fingerprint_hash,
    device_type,
    client_reported_coordinates,
    metadata,
    created_at,
    updated_at
  )
  VALUES (
    p_reservation_id,
    v_phase,
    v_user_id,
    v_participant_role,
    coalesce(nullif(trim(p_attestation_text), ''), 'Je certifie etre physiquement present avec l?autre partie au moment de la prise des photos. Toute fausse declaration engage ma responsabilite.'),
    true,
    now(),
    nullif(trim(coalesce(p_ip_address, '')), ''),
    nullif(trim(coalesce(p_user_agent, '')), ''),
    nullif(trim(coalesce(p_device_fingerprint_hash, '')), ''),
    nullif(trim(coalesce(p_device_type, '')), ''),
    nullif(trim(coalesce(p_client_reported_coordinates, '')), ''),
    coalesce(p_metadata, '{}'::jsonb),
    now(),
    now()
  )
  ON CONFLICT (reservation_id, phase, participant_role)
  DO UPDATE SET
    user_id = excluded.user_id,
    attestation_text = excluded.attestation_text,
    attestation_accepted = true,
    confirmed_at_server = now(),
    ip_address = excluded.ip_address,
    user_agent = excluded.user_agent,
    device_fingerprint_hash = excluded.device_fingerprint_hash,
    device_type = excluded.device_type,
    client_reported_coordinates = excluded.client_reported_coordinates,
    metadata = excluded.metadata,
    updated_at = now();

  INSERT INTO public.reservation_inspection_sessions (
    reservation_id,
    phase,
    owner_presence_confirmed_at,
    renter_presence_confirmed_at
  )
  VALUES (
    p_reservation_id,
    v_phase,
    CASE WHEN v_participant_role = 'owner' THEN now() ELSE NULL END,
    CASE WHEN v_participant_role = 'renter' THEN now() ELSE NULL END
  )
  ON CONFLICT (reservation_id, phase)
  DO UPDATE SET
    owner_presence_confirmed_at = CASE
      WHEN v_participant_role = 'owner'
      THEN coalesce(public.reservation_inspection_sessions.owner_presence_confirmed_at, now())
      ELSE public.reservation_inspection_sessions.owner_presence_confirmed_at
    END,
    renter_presence_confirmed_at = CASE
      WHEN v_participant_role = 'renter'
      THEN coalesce(public.reservation_inspection_sessions.renter_presence_confirmed_at, now())
      ELSE public.reservation_inspection_sessions.renter_presence_confirmed_at
    END,
    updated_at = now()
  RETURNING * INTO v_session;

  SELECT s.*
  INTO v_session
  FROM public.reservation_inspection_sessions s
  WHERE s.reservation_id = p_reservation_id
    AND s.phase = v_phase;

  RETURN v_session;
END;
$$;

