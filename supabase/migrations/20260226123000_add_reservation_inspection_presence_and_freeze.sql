CREATE OR REPLACE FUNCTION public.freeze_phase_reservation_photos_on_session_close()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public, auth, pg_temp
AS $$
BEGIN
  IF NEW.closed_at IS NOT NULL AND (
    TG_OP = 'INSERT'
    OR OLD.closed_at IS NULL
    OR OLD.closed_at IS DISTINCT FROM NEW.closed_at
  ) THEN
    UPDATE public.reservation_photos p
    SET
      is_frozen = true,
      frozen_at = coalesce(NEW.closed_at, now())
    WHERE p.reservation_id = NEW.reservation_id
      AND lower(coalesce(p.phase, '')) = lower(coalesce(NEW.phase, ''))
      AND (p.is_frozen IS DISTINCT FROM true OR p.frozen_at IS NULL);
  END IF;

  RETURN NEW;
END;
$$;

